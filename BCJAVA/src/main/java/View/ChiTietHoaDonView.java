    // CLICK ROW TABLE
    private void initLocalEvents() {
        tblCTHD.getSelectionModel().addListSelectionListener(e -> {
            int row = tblCTHD.getSelectedRow();
            if (row >= 0) {
                cbMaSP.setSelectedItem(model.getValueAt(row, 0));
                txtTenSP.setText(model.getValueAt(row, 1).toString());
                cbSL.setSelectedItem(Integer.parseInt(model.getValueAt(row, 2).toString()));
                txtDG.setText(model.getValueAt(row, 3).toString());
            }
        });
    }

    private void btnThoatActionPerformed(java.awt.event.ActionEvent evt) {
        if (parent != null) parent.setVisible(true);
        dispose();
    }
    
    



