    public ChiTietHoaDonView(HoaDonView parent, String maHD) {
        this();
        this.parent = parent;
        this.maHD = maHD;

        setTitle("Chi tiết hóa đơn - " + maHD);
        new CTHDController(this, maHD, parent);
    }

    private void initTable() {
        model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{
                "Mã SP", "Tên SP", "Số lượng", "Đơn giá", "Thành tiền"
        });
        tblCTHD.setModel(model);
    }

    public void loadTable(List<ChiTietHoaDon> list) {
        model.setRowCount(0);
        for (ChiTietHoaDon c : list) {
            model.addRow(new Object[]{
                    c.getMaSP(),
                    c.getTenSP(),
                    c.getSoLuong(),
                    c.getDonGia(),
                    c.getThanhTien()
            });
        }
    }

    // Lấy dữ liệu nhập
    public ChiTietHoaDon getInput(String maHD) {
        try {
            String maSP = (String) cbMaSP.getSelectedItem();
            String tenSP = txtTenSP.getText().trim();
            int soLuong = Integer.parseInt(cbSL.getSelectedItem().toString());

            double donGia = Double.parseDouble(txtDG.getText().trim());

            return new ChiTietHoaDon(maHD, maSP, tenSP, soLuong, donGia);

        } catch (Exception e) {
            showMessage("Dữ liệu nhập không hợp lệ!");
            return null;
        }
    }

    public String getSelectedMaSP() {
        int row = tblCTHD.getSelectedRow();
        if (row == -1) return null;
        return model.getValueAt(row, 0).toString();
    }

