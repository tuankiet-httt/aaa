public void setForm(DoanhThu kh) {
    txtMaKH.setText(kh.getMaKH());
    txtHoTen.setText(kh.getTenKH());
    txtSDT.setText(kh.getSdt());
    txtDiaChi.setText(kh.getDiaChi());
}
public DoanhThu getSelected() {
    int row = tblDSKH.getSelectedRow();
    if (row < 0) return null;

    return new DoanhThu(
        tblDSKH.getValueAt(row, 0).toString(),
        tblDSKH.getValueAt(row, 1).toString(),
        tblDSKH.getValueAt(row, 2).toString(),
        tblDSKH.getValueAt(row, 3).toString()
    );
}

    
