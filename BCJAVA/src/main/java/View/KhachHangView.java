public void tableClick() {
    tblDSKH.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            int row = tblDSKH.getSelectedRow();
            if (row >= 0) {
                txtMaKH.setText(tblDSKH.getValueAt(row, 0).toString());
                txtHoTen.setText(tblDSKH.getValueAt(row, 1).toString());
                txtSDT.setText(tblDSKH.getValueAt(row, 2).toString());
                txtDiaChi.setText(tblDSKH.getValueAt(row, 3).toString());
            }
        }
    });
}

